import type { FunctionComponent } from "react";
import Head from "next/head";
import PageWithBackButton from "components/Pages/PageWithBackButton";
import { useRouter } from "next/router";
import useLeague from "lib/league/useLeague";
import LeagueCard from "components/League/LeagueCard";

interface LeagueProps {}

const League: FunctionComponent<LeagueProps> = () => {
  const router = useRouter();
  const [league, loading, error] = useLeague(String(router.query.league));
  return (
    <>
      <Head>
        <title>League</title>
        <meta name="description" content="Generated by create next app" />
      </Head>
      <PageWithBackButton pageTitle={league ? league.name : ""}>
        {league && <LeagueCard league={league} />}
      </PageWithBackButton>
    </>
  );
};

export default League;
